    <p>Olá! Que bom receber sua visita. Tenho como <b>missão</b> promover um espaço de acolhimento seguro para você e ser uma facilitadora para seu autoconhecimento e desenvolvimento pessoal.</p>

<b><p>Experiência em:</b>
- Acolhimento, orientação e análise para adultos, adolescentes, gestantes ou puérperas nas demandas como depressão, ansiedade, angústias, tristezas, medos, dificuldades nas relações, autoconhecimento, desenvolvimento pessoal e baixa autoestima.</p>

<p>- Orientação profissional na primeira escolha da profissão ou reorientação de carreira.</p>

<p>- Auxílio para mulheres gestantes, puérperas e suas famílias para o fortalecimento da autoestima frente aos desafios que é a chegada de um novo bebê.</p>

<b><p>Serviços:</b> Orientação Psicológica, Psicoterapia breve, Psicoterapia, Autoconhecimento Breve, Pré-Natal Psicológico, Apoio Psicológico no puerpério e Orientação Profissional e de carreira.</p>

<p><b>Psicólogo</b> é para todo o mundo! Promova um tempo de autocuidado e autoconhecimento para si mesmo.</p> 


<p>Todos os atendimentos são <b>exclusivamente de modo Online.</b></p> 


<p><b>O atendimento Online</b> é realizado com conforto e segurança. Pode ser realizado por chamada de Vídeo (WhatsApp) ou por chamada telefônica, alguns serviços são realizados pelo Google meet.</p>


<b><p>Público Atendido:</b> Os atendimentos são somente para adolescentes, adultos brasileiros residentes no Brasil ou exterior, gestantes e mulheres no puerpério.</p>



<b><p>- Detalhes dos Serviços:</p></b>


<p><b>Psicoterapias: Como são os atendimentos e como os atendimentos podem me auxiliar?</b></p>

<p>A técnica utilizada é de Psicoterapia de <b>orientação psicanalítica</b>  As sessões se tornam uma parada para escutar a si mesmo e refletir com maior profundidade as circunstâncias do cotidiano e da história de vida. Nesse sentido, poderá haver uma ampliação da compreensão sobre suas dúvidas, seus sentimentos, desejos, angústias e conflitos inconscientes. A análise dessas circunstâncias pode propiciar outras possibilidades de ação e tomadas de atitudes,  possível melhoria nas relações, e na saúde emocional.</p>

<p>A dificuldade de esclarecer todos esses aspectos, e a falta de autoconhecimento e apoio, pode contribuir para  adoecimentos psicólogicos, como por exemplo, a ansiedade e a depressão.</p> 
 


<p><b>Tipos de Psicoterapia e quando utilizá-las:</b>
<ul style="margin-left: 26px;"><li><b>Orientação Psicológica</b> 
1 a 4 sessões.  É uma orientação e acolhimento em algum momento de necessidade. Público indicado: Adultos, Adolescentes, Gestantes e Puérperas (mulheres no pós-parto).</li></ul>

<ul style="margin-left: 26px;"><li><b>Psicoterapia Breve:</b> 
Em torno de 20 sessões. O objetivo é a intervenção em alguma questão-problema ou área específica e delimitada a ser desenvolvida. Como por exemplo: Problema profissional ou problema de relacionamento.  Público indicado: Adultos, Adolescentes, Gestantes e Puérperas (mulheres no pós-parto).</li></ul>

<ul style="margin-left: 26px;"><li><b>Psicoterapia:</b>  
Sem prazo pré-determinado. O objetivo da psicoterapia é  possibilitar ser trabalhado, refletido e analisar - com maior profundidade - sobre diversos temas e aspectos a desenvolver.  Público indicado: Adultos, Adolescentes, Gestantes e Puérperas (mulheres no pós-parto).</li></ul></p>                    



<p><b>Serviços de prevenção e quando utilizá-los:</b></p> 


<p><b>Autoconhecimento Breve:</b>  Para aqueles que não querem entrar em processo de psicoterapia e almejam saber um pouco mais de si mesmo, essa é uma das possibilidades. O objetivo desse programa é propiciar a facilitação ao autoconhecimento de forma breve, pois são em torno de 4 encontros online.  Através das atividades propostas o participante poderá se conectar com suas habilidades e alguns aspectos de sua personalidade, bem como a avaliação da saúde emocional atual.</p>

<p><b>Pré-Natal Psicológico:</b>    O objetivo desse programa é trabalhar o preparo emocional para a gestação e pós-parto da mulher gestante e da sua familia. Os temas dos 7 encontros são focados nos aspectos emocionais da gestação, pós-parto, maternidade e desenvolvimento emocional do bebê. São 7 workshops (encontros virtuais) de aproximadamente 1:30 hora de duração em cada encontro.</p>

<p><b>Apoio psicológico para mulheres no puerpério:</b>  O puerpério é um momento de muitas adaptações, inseguranças e por vezes instabilidades emocionais. Contar com um apoio psicológico nesse momento pode ser muito importante para a mulher. O apoio pode ser semanal, quinzenal ou esporádico, de acordo com as possibilidades da mulher.</p>


<p><b>Orientação Profissional e de Carreira:</b></p>

<p><b>Para quê serve?</b></p>

<p>A orientação profissional e orientação de carreira possibilitam um autoconhecimento pessoal e profissional para te auxiliar melhor em suas escolhas profissionais. Durante o processo da orientação vamos alinhando juntos as suas características de sua personalidade, bem como suas habilidades e aptidões, seus desejos e experiências de vida.</p>  

<p><b>Para Quem é indicada a Orientação Profissional e de carreira?</b></p>  

<p>As pessoas que estão escolhendo sua primeira profissão (jovens a partir do ensino médio)
As pessoas que estão no início de suas carreiras (estudantes do ensino técnico, estudantes da graduação ou recém-formados) 
 As pessoas que já têm uma trajetória profissional e almejam mudar de atividade, ou mesmo acrescentar uma nova atividade dentro de um curto, médio, ou longo prazo. 
 Aqueles que saíram ou estão saindo do mercado de trabalho e/ou almejam se aposentar e realizar outras tarefas profissionais</p>   

<p><b>Como é feita a orientação e o que é necessário?</b></p>

<p>São em torno de 6 encontros online, com duração entre 1:00 e 1:30 hrs, de acordo com a singularidade e necessidade de cada pessoa. É preciso ter um computador, internet e estar em um local tranqüilo e privativo. Serão pedidas algumas atividades extras. O orientando precisa estar envolvido e responder com maior sinceridade possível todas as perguntas das atividades solicitadas.</p>

<p><b>O que esperar do programa de orientação? Quais são as vantagens e Benefícios?</b></p> 

<p>Todo o processo de orientação é realizado por psicóloga atuante na clínica e com experiência em organizações, do qual vai te auxiliar melhor em seu autoconhecimento pessoal (as características da sua personalidade, suas habilidades, a sua saúde mental e investigação do histórico profissional/estudantil). Com essas avaliações a orientação servirá como uma bússola norteadora e uma escolha profissional mais consciente, ajudando possivelmente em um melhor desempenho profissional, bem como poderá construir metas e objetivos futuros. Ao final da orientação você vai receber todas as suas informações que foram levantadas durante os encontros em formato de material digital, assim você poderá recorrer ao material em qualquer fase de sua vida e principalmente que envolva escolha profissional e então você poderá refletir e decidir com maior amplitude diante de possíveis novas e futuras ocupações.</p>

